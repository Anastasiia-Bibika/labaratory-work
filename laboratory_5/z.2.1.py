import math
# Завдання 11.  Побудувати алгоритм для визначення того, чи утворюють цифри геометричну прогресію.
"""
n - кількість елементів
"""
dob = 0
n = int(input('Введіть кількість елементів: '))
for i in range(n):
    num = float(input('Ввести перше значення: '))
    num_1 = float(input('Ввести друге значення: '))
    num_2 = float(input('Ввести третє значення: '))
    dob = math.sqrt(num * num_2)
    if num_1 == dob:
        print('Утворюють геометричну прогресію')
    else:
        break